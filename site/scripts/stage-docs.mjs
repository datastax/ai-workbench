#!/usr/bin/env node
/**
 * Stage `<repo>/docs/*.md` into `<site>/.docs-staged/` so VitePress can
 * render them, and synthesize the landing page (`index.md`) on the
 * same pass.
 *
 * Why staging: the docs at `<repo>/docs/` are the source-of-truth on
 * GitHub. They render fine on github.com and shouldn't carry
 * VitePress-specific frontmatter or directives. This script copies
 * them verbatim — VitePress doesn't *need* frontmatter to render a
 * markdown page (it picks the page title up from the first H1), so
 * the canonical files stay clean.
 *
 * `index.md` is generated here too because it's a VitePress-specific
 * thing (uses the `home` layout with hero/features) — it has no
 * counterpart in `<repo>/docs/` and shouldn't.
 */

import { mkdir, readdir, readFile, rm, writeFile } from "node:fs/promises";
import { extname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = fileURLToPath(new URL(".", import.meta.url));
const REPO_ROOT = resolve(HERE, "../..");
const DOCS_SRC = resolve(REPO_ROOT, "docs");
const STAGED = resolve(HERE, "..", ".docs-staged");
// `README.md` is the docs-folder index (TOC) and points at relative
// paths from the docs/ folder; it doesn't make sense as a route on
// the site. Skip.
const SKIP = new Set(["README.md"]);

await rm(STAGED, { recursive: true, force: true });
await mkdir(STAGED, { recursive: true });

// Symlink the VitePress config so it lives next to the staged
// markdown files. The config itself lives in `site/.vitepress/` (so
// it's tracked); we mirror it into `.docs-staged/.vitepress/` so
// `vitepress build .docs-staged` finds it.
await mkdir(join(STAGED, ".vitepress"), { recursive: true });
await writeFile(
	join(STAGED, ".vitepress", "config.ts"),
	`// Re-export of site/.vitepress/config.ts so VitePress finds the\n` +
		`// config when invoked with srcDir = .docs-staged. Generated by\n` +
		`// scripts/stage-docs.mjs — don't edit by hand.\n` +
		`export { default } from "../../.vitepress/config.ts";\n`,
	"utf8",
);

const entries = await readdir(DOCS_SRC, { withFileTypes: true });
let count = 0;
for (const entry of entries) {
	if (!entry.isFile()) continue;
	if (extname(entry.name) !== ".md") continue;
	if (SKIP.has(entry.name)) continue;
	const body = await readFile(join(DOCS_SRC, entry.name), "utf8");
	await writeFile(join(STAGED, entry.name), body, "utf8");
	count += 1;
}

// Custom landing page using VitePress's `home` layout. Hand-rolled
// here rather than living in the canonical docs/ — it's
// VitePress-specific markup that doesn't belong in the source-of-
// truth markdown.
const indexMd = `---
layout: home

hero:
  name: AI Workbench
  text: One HTTP contract. Language-native runtimes.
  tagline: |
    Self-hosted runtime + UI for building knowledge bases on top of
    DataStax Astra. Workspaces, catalogs, vector stores, hybrid +
    rerank search, sync/async ingest with SSE progress, browser
    playground — all in one Docker image.
  actions:
    - theme: brand
      text: Read the architecture
      link: /architecture
    - theme: alt
      text: View on GitHub
      link: https://github.com/datastax/ai-workbench

features:
  - icon: 🟩
    title: One contract, N runtimes
    details: |
      The HTTP API is the contract. The default TypeScript runtime
      ships bundled with the UI; alternative-language "green boxes"
      (Python, Java) live as siblings under \`runtimes/\` and pass the
      same fixture-enforced conformance suite.
  - icon: 🔌
    title: Pluggable control plane
    details: |
      Workspaces, catalogs, and vector-store descriptors persist in
      one of three backends — \`memory\` (default), \`file\` (JSON on
      disk), or \`astra\` (Data API Tables) — behind one API contract.
  - icon: 🚀
    title: Astra-native, no wrappers
    details: |
      The TypeScript runtime uses \`@datastax/astra-db-ts\` directly;
      the Python runtime will use \`astrapy\`. No middleware, no
      shimming — each runtime speaks its language's native SDK.
  - icon: 🧪
    title: Browser playground + ingest
    details: |
      The bundled web UI runs vector / text / hybrid / rerank queries
      against any workspace's vector store, and ingests documents
      through a chunk → embed → upsert pipeline that streams progress
      via SSE.
  - icon: 🔐
    title: Secrets by reference
    details: |
      Credentials never live in records — fields like \`credentialsRef\`
      hold \`provider:path\` pointers (\`env:FOO\`, \`file:/path\`) resolved
      on demand by a pluggable \`SecretResolver\`.
  - icon: 🔁
    title: OIDC + silent refresh
    details: |
      The auth middleware handles workspace-scoped \`wb_live_*\` API
      keys and OIDC bearer tokens, with a browser login flow (PKCE)
      and silent token refresh so users don't see mid-session
      re-logins.
---

## Quickstart

\`\`\`bash
# Clone + install
git clone https://github.com/datastax/ai-workbench.git
cd ai-workbench
npm ci && npm run install:ts

# Boot with the default in-memory control plane
npm run dev                            # http://localhost:8080

# Hit it
curl http://localhost:8080/healthz     # {"status":"ok"}
curl http://localhost:8080/docs        # Scalar-rendered API reference
\`\`\`

Switching to an Astra-backed control plane is a YAML change — see
[Configuration](/configuration).
`;

await writeFile(join(STAGED, "index.md"), indexMd, "utf8");

console.log(`stage-docs: wrote ${count} pages + index.md to ${STAGED}`);
